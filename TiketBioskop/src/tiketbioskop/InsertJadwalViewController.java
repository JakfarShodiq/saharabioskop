    private void dataJadwal() {
        DefaultTableModel viewField=new DefaultTableModel();
        viewField.addColumn("ID Jadwal");
        viewField.addColumn("Judul Film");
        viewField.addColumn("Studio");
        viewField.addColumn("Jam");
        viewField.addColumn("Tanggal");
        viewField.addColumn("Harga");
        try {
            res=stat.executeQuery("select tj.id_jadwal as id_jadwal, tf.judul_film as judul_film, ts.nama_studio as nama_studio, tj.jam as jam, tj.date as tanggal, tj.harga as harga\n" +
"from tb_jadwal tj, tb_film tf, tb_studio ts\n" +
"where tj.id_film=tf.id_film and\n" +
"tj.id_studio=ts.id_studio\n" +
"order by 1 desc;");
           while(res.next()) {
               viewField.addRow(new Object[]{res.getString("id_jadwal"),res.getString("judul_film"),res.getString("nama_studio"),res.getString("jam"),res.getString("tanggal"),res.getString("harga")});
               
           } 
                jTable1.setModel(viewField);
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
